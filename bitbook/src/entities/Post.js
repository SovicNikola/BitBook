class Post {
    constructor(id, dateCreated, userId, userDisplayName, type, commentsNum) {
        this.id = id;
        this.dateCreated = dateCreated;
        this.userId = this.userId;
        this.userDisplayName = this.userDisplayName;
        this.type = type;
        this.commentsNum = commentsNum;

    }
}

export default Post;